#include<stdio.h>
void main()
{
char ch ;
printf("Enter the Alphabet : ");
scanf("%c",&ch);
switch(ch)
{
case 'A' :
printf("Vowel : A");
break;
case 'a' :
printf("Vowel : a");
break;
case 'E' :
printf("Vowel : E");
break;
case 'e' :
printf("Vowel : e");
break;
case 'I' :
printf("Vowel : I");
break;
case 'i' :
printf("Vowel : i");
break;
case 'O' :
printf("Vowel : O");
break;
case 'o' :
printf("Vowel : o");
break;
case 'U' :
printf("Vowel : U");
break;
case 'u' :
printf("Vowel : u");
break;
default:
printf("Constant");
}
}
